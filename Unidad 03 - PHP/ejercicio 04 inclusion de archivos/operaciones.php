<?php
  function sumar ($num1, $num2)
  {
    return $num1 + $num2;
  }

  function restar ($num1, $num2)
  {
    return $num1 - $num2;
  }

  function multiplica ($num1, $num2)
  {
    return $num1 * $num2;
  }

  function dividir ($num1, $num2)
  {
    return $num1 / $num2;
  }

  function elevar ($num1, $num2)
  {
    return $num1 ** $num2;
  }
?>
